#!/usr/bin/env python3
"""
Generate synthetic 3D microstructure datasets for OpenImpala regression benchmarks.

These datasets have known analytical solutions for effective transport properties,
making them suitable for automated CI validation.

Geometries generated (all 32x32x32 UINT8 RAW files):

  1. uniform_block.raw
     All voxels = 1 (single conducting phase).
     Analytical: tau = (N-1)/N = 0.96875 for N=32.

  2. layers_series_x.raw
     Alternating single-voxel layers along X (flow direction).
     Phase 0 (voxel=0, D=1.0) and Phase 1 (voxel=1, D=0.5).
     Analytical: D_eff = harmonic mean = 2*D0*D1/(D0+D1) = 2/3
                 tau = (N-1)*(D0+D1) / (2*N*D0*D1) = 1.453125

  3. layers_parallel_y.raw
     Alternating single-voxel layers along Y (perpendicular to flow).
     Phase 0 (voxel=0, D=1.0) and Phase 1 (voxel=1, D=0.5).
     Analytical: D_eff = arithmetic mean = (D0+D1)/2 = 0.75
                 tau = 2*(N-1) / (N*(D0+D1)) = 1.291667

Usage:
    python3 generate_benchmarks.py [--size 32] [--outdir ./benchmark_data]

These RAW files can be used with the production Diffusion binary:
    mpirun -np 1 Diffusion inputs_file
with raw_width/raw_height/raw_depth = 32 and raw_datatype = UINT8.

Note: The CI regression tests (tRegressionBenchmark_*) create these same
geometries in-memory and do NOT require these files. This script is provided
for manual testing, visualization, and documentation purposes.
"""

import argparse
import os
import struct
import sys


def write_raw_uint8(filepath, data_3d, N):
    """Write a 3D array as a flat UINT8 RAW file (Z-slowest, X-fastest)."""
    with open(filepath, "wb") as f:
        for k in range(N):
            for j in range(N):
                for i in range(N):
                    f.write(struct.pack("B", data_3d[k][j][i]))


def generate_uniform_block(N):
    """All voxels = 1 (single conducting phase)."""
    return [[[1 for _ in range(N)] for _ in range(N)] for _ in range(N)]


def generate_layers_series_x(N):
    """Alternating layers along X: even X → phase 0, odd X → phase 1."""
    return [[[i % 2 for i in range(N)] for _ in range(N)] for _ in range(N)]


def generate_layers_parallel_y(N):
    """Alternating layers along Y: even Y → phase 0, odd Y → phase 1."""
    return [[[j % 2 for _ in range(N)] for j in range(N)] for _ in range(N)]


def write_inputs_file(filepath, raw_filename, N, description, D0, D1, expected_tau):
    """Write an OpenImpala inputs file for a benchmark dataset."""
    content = f"""\
# Regression Benchmark: {description}
# Auto-generated by generate_benchmarks.py

filename = "{raw_filename}"
data_path = "."
raw_width = {N}
raw_height = {N}
raw_depth = {N}
raw_datatype = UINT8

phase_id = 0
threshold_value = 0.5
direction = X
solver_type = FlexGMRES
box_size = 16
verbose = 1
results_dir = ./benchmark_results/
output_filename = {os.path.splitext(os.path.basename(raw_filename))[0]}_results.txt

hypre.maxiter = 500
hypre.eps = 1e-9

# Expected tortuosity: {expected_tau:.6f}
# Phase 0: D = {D0}, Phase 1: D = {D1}
"""
    with open(filepath, "w") as f:
        f.write(content)


def main():
    parser = argparse.ArgumentParser(
        description="Generate synthetic benchmark datasets for OpenImpala regression tests."
    )
    parser.add_argument("--size", type=int, default=32, help="Domain size N (NxNxN voxels)")
    parser.add_argument("--outdir", type=str, default="./benchmark_data",
                        help="Output directory for generated files")
    args = parser.parse_args()

    N = args.size
    outdir = args.outdir
    os.makedirs(outdir, exist_ok=True)

    D0, D1 = 1.0, 0.5
    D_h = 2.0 * D0 * D1 / (D0 + D1)
    D_a = (D0 + D1) / 2.0

    benchmarks = [
        {
            "name": "uniform_block",
            "generator": generate_uniform_block,
            "description": "Uniform Dense Block (single phase, D=1.0)",
            "D0": 1.0, "D1": 1.0,
            "tau": (N - 1) / N,
        },
        {
            "name": "layers_series_x",
            "generator": generate_layers_series_x,
            "description": f"Series Resistance (layers along X, D0={D0}, D1={D1})",
            "D0": D0, "D1": D1,
            "tau": (N - 1) * (D0 + D1) / (2 * N * D0 * D1),
        },
        {
            "name": "layers_parallel_y",
            "generator": generate_layers_parallel_y,
            "description": f"Parallel Resistance (layers along Y, D0={D0}, D1={D1})",
            "D0": D0, "D1": D1,
            "tau": 2 * (N - 1) / (N * (D0 + D1)),
        },
    ]

    print(f"Generating {len(benchmarks)} benchmark datasets ({N}^3 = {N**3} voxels each)")
    print(f"Output directory: {os.path.abspath(outdir)}\n")

    for bm in benchmarks:
        raw_path = os.path.join(outdir, f"{bm['name']}.raw")
        inputs_path = os.path.join(outdir, f"{bm['name']}.inputs")

        data = bm["generator"](N)
        write_raw_uint8(raw_path, data, N)
        write_inputs_file(inputs_path, raw_path, N, bm["description"],
                          bm["D0"], bm["D1"], bm["tau"])

        file_size = os.path.getsize(raw_path)
        print(f"  {bm['name']}:")
        print(f"    RAW file:    {raw_path} ({file_size} bytes)")
        print(f"    Inputs file: {inputs_path}")
        print(f"    Description: {bm['description']}")
        print(f"    Expected tau: {bm['tau']:.6f}")
        print()

    print("Analytical reference values:")
    print(f"  Harmonic mean D_h = 2*{D0}*{D1}/({D0}+{D1}) = {D_h:.6f}")
    print(f"  Arithmetic mean D_a = ({D0}+{D1})/2 = {D_a:.6f}")
    print(f"  Discrete correction factor: N/(N-1) = {N/(N-1):.6f}")
    print()
    print("Hashin-Shtrikman bounds for reference (VF=0.5):")
    hs_lower = D1 + 0.5 / (1.0 / (D0 - D1) + 0.5 / (3.0 * D1))
    hs_upper = D0 + 0.5 / (1.0 / (D1 - D0) + 0.5 / (3.0 * D0))
    print(f"  HS lower bound: D_eff >= {hs_lower:.6f}")
    print(f"  HS upper bound: D_eff <= {hs_upper:.6f}")
    print(f"  (Series D_eff = {D_h:.6f}, Parallel D_eff = {D_a:.6f})")


if __name__ == "__main__":
    main()
